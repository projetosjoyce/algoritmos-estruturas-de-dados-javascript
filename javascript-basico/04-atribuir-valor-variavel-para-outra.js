/* Atribuir o valor de uma variável para outra */

/*

Após um valor ser atribuído para uma variável usando o operador de atribuição, 
você pode atribuir o valor daquela variável para outra variável usando o operador de atribuição.

Exemplo:

var myVar;
myVar = 5;
var myNum;
myNum = myVar;

*/

//Exemplo:

var minhaVariavel; // declarei
minhaVariavel = 10; //atribui valor 

var meuNumero; //declarei outra variavel 
meuNumero = minhaVariavel; // a variavel meuNumero recebe o valor da minhaVariavel
console.log(meuNumero); //10