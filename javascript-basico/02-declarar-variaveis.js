/* Declarar variáveis JavaScript */

/*
Na ciência da computação, dado é qualquer coisa que tenha significado para o computador. 
JavaScript fornece oito tipos de dados diferentes que são undefined, null, boolean, string, 
symbol, bigint, number e object.

Por exemplo, os computadores distinguem números, como o número12, e strings, como o "12", "dog", 
ou"123 cats", as quais são coleções de caracteres. Computadores podem realizar operações matemáticas 
em um número, mas não em uma string.

Variáveis permitem aos computadores armazenar e manipular dados de forma dinâmica. Elas fazem isso 
usando uma "etiqueta" para apontar para o dado ao invés de usar o próprio dado. Qualquer um dos 8 
tipos de dados pode ser armazenado em uma variável.

Variáveis são similares às variáveis x e y utilizadas na matemática, o que significa que elas são 
simples nomes para representar os dados aos quais se referem. Variáveis de computador diferem das 
variáveis matemáticas porque elas podem armazenar diferentes valores em momentos diferentes.

Dizemos ao JavaScript para criar ou declarar uma variável colocando a palavra-chave var na frente dela, 
dessa forma:

var ourName;

*/

//Exemplo:

var declarandoVariavel; // aqui só declarei
console.log(declarandoVariavel); //undefined

var declarandoVariavel2 = "agoraAVariavelTemValor"; //aqui declarei e atribui um valor 
console.log(declarandoVariavel2); //agoraAVariavelTemValor

